// Add parallax effect to hero background
window.addEventListener('scroll', () => {
    const scrolled = window.pageYOffset;
    const heroAnimation = document.querySelector('.hero-bg-animation');
    if (heroAnimation) {
        heroAnimation.style.transform = `translateY(${scrolled * 0.5}px)`;
    }
});

// Add dynamic greeting based on time of day
const updateGreeting = () => {
    const hour = new Date().getHours();
    let greeting = 'Hello';
    
    if (hour < 12) {
        greeting = 'Good morning';
    } else if (hour < 18) {
        greeting = 'Good afternoon';
    } else {
        greeting = 'Good evening';
    }
    
    // You can use this greeting somewhere on the page if needed
    console.log(greeting + '! Welcome to PulseHive Media');
};

updateGreeting();

